'''
    Belajar Operator Pemabanding
    == (sinilai)
    != (tidak sama)
    > (lebih besar)
    < (lebih kecil)
    >= (lebih besar sama dengan)
    <= (lebih kecil sama dengan)
'''
nilai_a = int(input("Masukan Nilai Pertama = "))
nilai_b = int(input("Masukan Nilai Kedua = "))


print("jika nilai pertama = nilai kedua maka : ", nilai_a == nilai_b)
print("jika nilai pertama tidak sama dengan nilai kedua maka : ", nilai_a != nilai_b)
print("jika nilai pertama lebih besar dari nilai kedua maka : ", nilai_a > nilai_b)
print("jika nilai pertama lebih kecil dari nilai kedua maka : ", nilai_a > nilai_b)
print("jika nilai pertama lebih besar sama dengan nilai kedua maka : ", nilai_a >= nilai_b)
print("jika nilai pertama lebih kecil sama dengan nilai kedua maka : ", nilai_a <= nilai_b)

